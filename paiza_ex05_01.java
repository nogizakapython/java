public class paiza_ex05_01 {
     public static void main(String... args) {
        // 足し算
        System.out.println(3+2);
        // 掛け算
        System.out.println(3*2);
        // 引き算
        System.out.println(3-2);
        // 割り算の商
        System.out.println(3/2);
        //  割り算の余り
        System.out.println(3%2);
    }
}
